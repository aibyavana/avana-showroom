export function LineFigure() {
  const ref = useRef<SVGSVGElement>(null);
  const inView = useInView(ref, { amount: 0.15, once: true });
  const reduce = useReducedMotion();
  const dashOffset = useMotionValue(PATH_LEN);

  useEffect(() => {
    if (reduce) {
      dashOffset.set(0);
      return;
    }
    if (!inView) return;

    // Reset to fully hidden, then wait one frame before starting the draw
    // so the reset is committed to the DOM regardless of when inView fires.
    dashOffset.set(PATH_LEN);
    const raf = requestAnimationFrame(() => {
      animate(dashOffset, 0, {
        duration: DUR_DRAW,
        ease: [...EASE_SULTRY] as [number, number, number, number],
      });
    });
    return () => cancelAnimationFrame(raf);
  }, [inView, reduce, dashOffset]);

  return (
    <svg
      ref={ref}
      viewBox={VIEWBOX}
      className="w-[340px] max-w-[60%] md:max-w-full mx-auto"
      aria-label="Single-line illustration of a figure in a wide-brim hat"
      role="img"
    >
      <g transform={TRANSFORM}>
        <motion.path
          d={FIGURE_PATH}
          fill="none"
          stroke="#B8902E"
          strokeWidth={22}
          strokeLinecap="round"
          strokeLinejoin="round"
          strokeDasharray={PATH_LEN}
          style={{ strokeDashoffset: dashOffset }}
        />
      </g>
    </svg>
  );
}
